package Challenge4;

public class swapTwoNumbers {
    public static void main(String[] args) {
        System.out.println();
        System.out.println("Welcome to Swap Station");
        int a = 5;
        System.out.println(a);
        int b = 6;
        System.out.println(b);
        int temp;
        temp = a;
        a = b;
        b = temp;
        System.out.println(" After Swap the Number :- ");
        System.out.println(a);
        System.out.println(b);
    }
}
