package TypeConversion;
public class firstProgram
{
    public static void main(String[] args)
    {
        System.out.println();
        float a = 4;
        System.out.println(a);

        int b = (int)5.89f;
        System.out.println(b);

        int c = 5;
        System.out.println((double)5);
    }
}
