package Functions;

public class basic_uses_of_functions {
    public static void main(String[] args)
    {
        System.out.println();
        System.out.println(" basic_uses_of_functions ");
        System.out.println();
        function1();
        System.out.println();
        function2();
        System.out.println();
        function3();

    }
    public static void function1()
    {

        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        System.out.println("* * * * *");
        }

    public  static void function2()
    {
        System.out.println("* * * * *");
        System.out.println("* * * *");
        System.out.println("* * *");
        System.out.println("* *");
        System.out.println("*");
    }
    public static void function3()
    {
        System.out.println("        *");
        System.out.println("      * *");
        System.out.println("    * * *");
        System.out.println("  * * * *");
        System.out.println("* * * * *");
    }
}
