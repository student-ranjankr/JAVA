package Operators_if_else_number_system;

public class unary_operators
{
    public static void main(String[] args)
    {
        System.out.println();
        int x = 7;
        int y = -x;
        int z = -y;
        System.out.println(z);
        System.out.println();
        System.out.println();
        int a = 8;
        ++a;
        System.out.println(a); //9
        a--;
        System.out.println(a); //8
        --a;
        System.out.println(a); //7
        a++;
        System.out.println(a); //8

    }
}
