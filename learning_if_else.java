package if_else;

public class learning_if_else {
    public static void main(String[] args) {
        System.out.println();
        System.out.println(" learning_if_else ");
        System.out.println();
        boolean isMale = true;
        String name = "Bob";
        System.out.println("before if");
        if(isMale)
        {
            System.out.println("Mr."+name);
        }
        else
        {
            System.out.println("Mrs."+name);
        }
        System.out.println("after if");
    }
}
