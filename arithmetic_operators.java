package Operators_if_else_number_system;

public class arithmetic_operators {
    public static void main(String[] args) {
        System.out.println();
        int a ;
        int b ;
        a = 89;
        b = 67;
        int c ;
        c = a+b;
        System.out.println(c);
        c = a-b;
        System.out.println(c);
        c = a*b;
        System.out.println(c);
        c = a/b;
        System.out.println(c);
        System.out.println();
        double x = 5;
        double y = 7;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println();

        //modulus operator;

        int m = 34;
        int n = 45;
        System.out.println(m%n);

    }
}
